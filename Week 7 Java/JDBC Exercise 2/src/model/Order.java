package model;

public class Order {
	Integer id;
	Integer Item_id;
	Integer Quantity;
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getItem_id() {
		return Item_id;
	}
	public void setItem_id(Integer item_id) {
		Item_id = item_id;
	}
	public Integer getQuantity() {
		return Quantity;
	}
	public void setQuantity(Integer quantity) {
		Quantity = quantity;
	}
	
	
}
