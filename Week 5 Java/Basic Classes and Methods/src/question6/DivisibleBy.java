package question6;

public class DivisibleBy {
	public static boolean iseven(int n) {
		if (n % 2 == 0) {
			return true;
		} else {
			return false;
		}
	}
	
	public static boolean isDivisibleBy3( int n ) {
		if (n % 3 == 0) {
			return true;
		} else {
			return false;
		}
	}
}
